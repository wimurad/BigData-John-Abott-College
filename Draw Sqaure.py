import turtle

def draw_square():
    window = turtle.Screen()
    window.bgcolor ("blue")

    abcd = turtle. Turtle()
    abcd.forward(200)
    abcd.right(90)
    abcd.forward(200)
    abcd.right(90)
    abcd.forward(200)
    abcd.right(90)
    abcd.forward(200)
    abcd.right(90)
    window.exitonclick()
draw_square()
    
    
